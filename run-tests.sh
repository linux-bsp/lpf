#!/bin/bash
# =============================================================================
# EMS 测试运行脚本
# =============================================================================
# 此脚本设置正确的库路径并运行测试程序
# =============================================================================

# 设置库路径
export LD_LIBRARY_PATH=.staging/lib:$LD_LIBRARY_PATH

# 检查测试程序是否存在
if [ ! -f .staging/bin/ems-unit-test ]; then
    echo "错误：测试程序不存在，请先运行 'make' 构建项目"
    exit 1
fi

# 运行测试程序
echo "运行单元测试..."
.staging/bin/ems-unit-test "$@"
